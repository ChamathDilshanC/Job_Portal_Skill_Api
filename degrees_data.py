def search_degrees(query: str, limit: int = 10):
    """
    Search degrees by query string

    Args:
        query: Search term
        limit: Maximum number of results to return

    Returns:
        List of matching degrees
    """
    if not query or not query.strip():
        return COMMON_DEGREES[:limit]

    search_term = query.lower().strip()

    # Find matches
    matches = [
        degree for degree in COMMON_DEGREES
        if search_term in degree.lower()
    ]

    return matches[:limit] if matches else COMMON_DEGREES[:limit]


def get_degrees_by_category(category: str):
    """
    Get degrees by category

    Args:
        category: Degree category (e.g., 'Undergraduate', 'Graduate')

    Returns:
        List of degrees in that category
    """
    return DEGREE_CATEGORIES.get(category, [])


def get_all_categories():
    """
    Get all degree categories

    Returns:
        Dictionary of all categories and their degrees
    """
    return DEGREE_CATEGORIES
